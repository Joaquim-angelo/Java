package com.ex016;

import java.util.Arrays;

public class Vetor {
    public static void main(String[] args){
        /*Criando um vetor*/
        int n[]={1,2,3,4,5};
        /*Lenght: comprimento*/
        System.out.println("O vetor tem "+n.length+" casas");
        for(int i=0;i<=5;i++){
            System.out.println(n[i]);
        }
    }
}
